document.querySelector('#aRead').addEventListener('click',sayAOutLoud)
function sayAOutLoud(){
    //get alphabet
    let sayA = document.getElementById('aRead').textContent;
    //speak
    let loud=new SpeechSynthesisUtterance(sayA);
    window.speechSynthesis.speak(loud);
}
